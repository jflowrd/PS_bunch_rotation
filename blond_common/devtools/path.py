import os


def makedir(dirname):
    '''
    Create folder at dirname
    '''

    # Try to create directory
    try:
        os.makedirs(dirname)
    # Check whether already exists/creation failed
    except OSError:
        if os.path.exists(dirname):
            pass
        else:
            raise
